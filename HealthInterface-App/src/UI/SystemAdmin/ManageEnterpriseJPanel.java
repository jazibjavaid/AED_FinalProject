    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        City city = (City) cityJComboBox.getSelectedItem();
        category = (Enterprise.EnterpriseCategory) enterpriseTypeJComboBox.getSelectedItem();

        if (city == null || category == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = txtName.getText();
        String zipcode = txtZipcode.getText();
        String contactNumber = txtContact.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String numberOfBeds = txtNumberOfBeds.getText();
        
        if(name.isEmpty() || zipcode.isEmpty() || contactNumber.isEmpty() || email.isEmpty() || address.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Some fields are missing. Please enter all fields", "Validation Error !",
                    JOptionPane.ERROR_MESSAGE); 
        }
        else{
        Enterprise enterprise = city.getEnterpriseDir().createAndAddEnterprise(numberOfBeds,name, contactNumber, email, address, zipcode, category);
        JOptionPane.showMessageDialog(null, name+" created successfully");
        populateTable();
        
        txtName.setText("");
        txtZipcode.setText("");
        txtContact.setText("");
        txtEmail.setText("");
        txtAddress.setText("");
        txtNumberOfBeds.setText("");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

