     private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (City city : system.getCityList()) {
            for (Enterprise enterprise : city.getEnterpriseDir().getEnterpriseList()) {
                Object[] row = new Object[7];
                row[0] = enterprise.getName();
                row[1] = enterprise.getEnterpriseCategory().getValue();
                row[2] = enterprise.getContactNumber();
                row[3] = enterprise.getEmail();
                row[4] = enterprise.getAddress();
                row[5] = enterprise.getZipcode();
                row[6] = city.getName();
                

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        cityJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();

        for (City city : system.getCityList()) {
            cityJComboBox.addItem(city);
        }

        for (Enterprise.EnterpriseCategory type : Enterprise.EnterpriseCategory.values()) {
            enterpriseTypeJComboBox.addItem(type);
        }
    }
